    private void loadSeat(List<Seat> seats, List<String> seatsBooked, Showtime st) {
        cartSeat = new ArrayList<>();
        jPanel20.removeAll();
        jPanel20.revalidate();
        jPanel20.repaint();
        jPanel20.setLayout(new GridLayout(10, 16, 10, 10));
        jPanel20.setBackground(new Color(243, 245, 247));
        for (int i = 0; i < seats.size(); i++) {
            Seat seat = seats.get(i);
            JPanel subPanel = new JPanel();
            subPanel.setPreferredSize(new Dimension(40, 40));
            subPanel.setLayout(new GridBagLayout());
            subPanel.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
            subPanel.setBackground(new Color(243, 245, 247));
            if (seatBooked(seat, seatsBooked)) {
                subPanel.setBackground(new Color(153, 153, 153));
            } else {
                if (seat.getSeatType() == SeatType.STANDARD) {
                    subPanel.setBorder(BorderFactory.createLineBorder(new Color(10, 139, 5)));
                } else {
                    subPanel.setBorder(BorderFactory.createLineBorder(Color.RED));
                }
            }
            JLabel lblNameSeat = new JLabel(seat.getName());
            lblNameSeat.setFont(new java.awt.Font("Segoe UI", 1, 12));
            subPanel.add(lblNameSeat);
            addFocusSeat(subPanel, seat, st);
            jPanel20.add(subPanel);
        }
    }

    private boolean seatBooked(Seat seat, List<String> seatsBooked) {
        for (String seatId : seatsBooked) {
            if (seat.getSeatId().equals(seatId)) {
                return true;
            }
        }
        return false;
    }

    private void addFocusSeat(JPanel subPanel, Seat seat, Showtime st) {
        if (st.getStartTime().isBefore(LocalDateTime.now())) {
            return;
        }
        subPanel.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                Color color = new Color(243, 245, 247);
                if (subPanel.getBackground().equals(new Color(153, 153, 153))) {
                    JOptionPane.showMessageDialog(null, "Ghế đã được đặt", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                if (subPanel.getBackground().equals(color)) {
                    if (cartSeat.size() == 8) {
                        JOptionPane.showMessageDialog(null, "Chỉ được chọn tối đa 8 ghế", "Lỗi", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                    subPanel.setBackground(new Color(10, 139, 5));
                    cartSeat.add(seat);
                } else {
                    subPanel.setBackground(color);
                    cartSeat.remove(seat);
                }
                loadSeatTabRight(cartSeat);
            }
        });
    }

    private void loadSeatTabRight(List<Seat> cartSeat) {
        String vip = "";
        String thuong = "";
        List<Seat> seatStandard = cartSeat.stream()
                .filter(x -> x.getSeatType() == SeatType.STANDARD)
                .toList();
        List<Seat> seatVip = cartSeat.stream()
                .filter(x -> x.getSeatType() == SeatType.VIP)
                .toList();

        for (int i = 0; i < seatStandard.size(); i++) {
            Seat s = seatStandard.get(i);
            if (s.getSeatType() == SeatType.STANDARD) {
                thuong += s.getName();
                if (i != seatStandard.size() - 1) {
                    thuong += ", ";
                }
            }
        }
        for (int i = 0; i < seatVip.size(); i++) {
            Seat s = seatVip.get(i);
            if (s.getSeatType() == SeatType.VIP) {
                vip += s.getName();
                if (i != seatVip.size() - 1) {
                    vip += ", ";
                }
            }
        }
        qtyThuong.setText(seatStandard.size() + "x");
        gheThuong.setText("Ghế: " + thuong);
        qtyVip.setText(seatVip.size() + "x");
        gheVip.setText("Ghế: " + vip);

        int quantityVip = Integer.parseInt(qtyVip.getText().substring(0, 1));
        int quantityThuong = Integer.parseInt(qtyThuong.getText().substring(0, 1));
        double priceVip = 0;
        double priceThuong = 0;
        try {
            priceVip = numberFormat.parse(txtGheVip.getText()).doubleValue();
            priceThuong = numberFormat.parse(txtGheThuong.getText()).doubleValue();
        } catch (ParseException ex) {
            ex.printStackTrace();
        }

        double total = quantityVip * priceVip + quantityThuong * priceThuong;

        txtTongTien.setText(total == 0 ? "0" : (formatter.format(total)));
    }
