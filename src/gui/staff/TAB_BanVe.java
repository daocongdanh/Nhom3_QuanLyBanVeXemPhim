    private void setVoucher(JTextField txtVoucher1, JPanel pnVoucher1, JLabel vcCode1,
            JLabel vcDPH1, JLabel vcNgayBatDau1, JLabel vcNgayKetThuc1,
            JLabel vcGiamGia1, JLabel vcToiThieu1) {
        String code = txtVoucher1.getText();
        if (code.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Voucher không được rỗng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Voucher voucher = voucherBUS.getVoucherByCode(code);
        if (voucher == null) {
            JOptionPane.showMessageDialog(null, "Không tìm thấy Voucher", "Lỗi", JOptionPane.ERROR_MESSAGE);
            pnVoucher1.setVisible(false);
            return;
        }
        if (pnVoucher1.isVisible() && !voucher.getCode().equals(vcCode1.getText())) {
            JOptionPane.showMessageDialog(null, "Chỉ được áp dụng một Voucher", "Lỗi", JOptionPane.ERROR_MESSAGE);
            txtVoucher1.setText("");
            return;
        }
        VoucherRelease voucherRelease = voucherReleaseBUS.getVoucherReleaseById(
                voucher.getVoucherRelease().getVoucherReleaseId());
        if (voucherRelease.getEndDate().isBefore(LocalDate.now())) {
            JOptionPane.showMessageDialog(null, "Voucher đã hết hạn sử dụng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            txtVoucher1.setText("");
            return;
        }
        if (voucher.isStatus()) {
            JOptionPane.showMessageDialog(null, "Voucher đã được sử dụng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            txtVoucher1.setText("");
            pnVoucher1.setVisible(false);
            return;
        }
        vcCode1.setText(code);
        vcDPH1.setText(voucherRelease.getReleaseName());
        vcNgayBatDau1.setText(voucherRelease.getStartDate().format(DateTimeFormatter.ofPattern("dd-MM-yyyy")));
        vcNgayKetThuc1.setText(voucherRelease.getEndDate().format(DateTimeFormatter.ofPattern("dd-MM-yyyy")));
        ObjectType objType = voucherRelease.getObjectType();
        String objectType = "Toàn bộ";
        if (objType == ObjectType.TICKET) {
            objectType = "Vé";
        } else if (objType == ObjectType.PRODUCT) {
            objectType = "Sản phẩm";
        }
        vcGiamGia1.setText(formatter.format(voucherRelease.getPrice()) + " đ (" + objectType + ")");
        vcToiThieu1.setText(formatter.format(voucherRelease.getMinPrice()) + " đ");
        txtVoucher1.setText("");
        double minPrice = voucherRelease.getMinPrice();
        double price = voucherRelease.getPrice();
        double khuyenMai = 0;
        double tongTienSp = 0;
        double tongTienVe = 0;
        double tongTienVeSp = 0;
        double tienThue = 0;
        try {
            tongTienVeSp = numberFormat.parse(ttTongTienVeSp.getText()).doubleValue();
            tongTienSp = numberFormat.parse(ttTongTienSp.getText()).doubleValue();
            tongTienVe = numberFormat.parse(ttTongTienVe.getText()).doubleValue();
            tienThue = numberFormat.parse(ttVAT.getText()).doubleValue();
        } catch (ParseException ex) {
            ex.printStackTrace();
        }
        if (objectType.equals("Toàn bộ")) {
            if (tongTienVeSp >= minPrice) {
                khuyenMai = price;
            }
        } else if (objectType.equals("Vé")) {
            if (tongTienVe >= minPrice) {
                khuyenMai = price;
            }
        } else {
            if (tongTienSp >= minPrice) {
                khuyenMai = price;
            }
        }
        ttKhuyenMai.setText(formatter.format(khuyenMai));
        ttTongTien.setText(formatter.format(tongTienVeSp + tienThue - khuyenMai));
        pnVoucher1.setVisible(true);

    }
