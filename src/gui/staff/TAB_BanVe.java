    private boolean updateUnstickClient(JLabel lblSoLuong, int inc) {
        int valueOld = Integer.parseInt(lblSoLuong.getText().substring(4));
        if (valueOld + inc < 0) {
            JOptionPane.showMessageDialog(null, "Hết hàng!", "", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        valueOld = valueOld + inc;
        lblSoLuong.setText("SL: " + valueOld);
        return true;
    }

    private ImageIcon scaleIcon(ImageIcon icon, int width, int height) {
        Image image = icon.getImage();
        Image imgScale = image.getScaledInstance(width, height, Image.SCALE_SMOOTH);
        ImageIcon scaledIcon = new ImageIcon(imgScale);
        return scaledIcon;
    }

