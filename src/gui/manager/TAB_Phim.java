    private boolean checkValid(JTextField txtTenPhimXem, JTextField txtThoiLuongXem, JTextField txtTacGiaXem,
            JTextField txtNgonNguXem, JTextField txtQuocGiaXem, JTextField txtNamXem, boolean update) {
        String tenPhim = txtTenPhimXem.getText();
        String thoiLuong = txtThoiLuongXem.getText();
        String tacGia = txtTacGiaXem.getText();
        String ngonNgu = txtNgonNguXem.getText();
        String quocGia = txtQuocGiaXem.getText();
        String nam = txtNamXem.getText().trim();

        if (tenPhim.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Tên phim không được rỗng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (thoiLuong.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Thời lượng phim không được rỗng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            try {
                int thoiLuong_parse = Integer.parseInt(thoiLuong);
                if (thoiLuong_parse <= 0) {
                    JOptionPane.showMessageDialog(null, "Thời lượng phim phải lớn hơn 0", "Lỗi", JOptionPane.ERROR_MESSAGE);
                    return false;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Thời lượng phim phải là số", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        }
        if (tacGia.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Tác giả không được rỗng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (ngonNgu.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Ngôn ngữ không được rỗng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (quocGia.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Quốc gia không được rỗng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (nam.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Năm phát hành không được rỗng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            try {
                int nam_parse = Integer.parseInt(nam);
                if (nam_parse <= 1900) {
                    JOptionPane.showMessageDialog(null, "Năm phát hành phải lớn hơn 1900", "Lỗi", JOptionPane.ERROR_MESSAGE);
                    return false;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Năm phát hành phải là số", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        }
        if (update == false && selectedFile == null) {
            JOptionPane.showMessageDialog(null, "Ảnh phim không được rỗng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

