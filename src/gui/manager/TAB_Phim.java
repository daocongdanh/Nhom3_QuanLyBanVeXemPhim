    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed

        if (checkValid(txtTenPhimThem, txtThoiLuongThem, txtTacGiaThem, txtNgonNguThem, txtQuocGiaThem, txtNamThem, false)) {
            String thoiLuong = txtThoiLuongThem.getText().trim();
            String theLoai = (String) jcbTheLoai.getSelectedItem();
            String tenPhim = txtTenPhimThem.getText().trim();
            String tacGia = txtTacGiaThem.getText().trim();
            String nam = txtNamThem.getText().trim();
            String quocGia = txtQuocGiaThem.getText().trim();
            String ngonNgu = txtNgonNguThem.getText().trim();
            String moTa = txtMoTa.getText().trim();
            if(movieBUS.existsByName(tenPhim)){
                JOptionPane.showMessageDialog(null, "Tên Phim đã tồn tại", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            String image = fileManager.createFile(selectedFile);
            if (image == null) {
                JOptionPane.showMessageDialog(null, "Không phải file ảnh", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Movie movie = new Movie(null, tenPhim, Integer.parseInt(thoiLuong), quocGia, ngonNgu, moTa,
                    Integer.parseInt(nam), tacGia, image, MovieStatus.PLAYING,
                    movieTypeBUS.getMovieTypeByName(theLoai));
            if (movieBUS.insertMovie(movie)) {
                JOptionPane.showMessageDialog(null, "Thêm mới thành công");
                loadData("", MovieStatus.PLAYING);
                modalThemPhim.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Thêm mới thất bại", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnSubmitActionPerformed

