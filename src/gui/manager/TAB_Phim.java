    private void btnSuaphimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaphimActionPerformed
        // TODO add your handling code here:
        if (checkValid(txtTenPhimXem, txtThoiLuongXem, txtTacGiaXem, txtNgonNguXem, txtQuocGiaXem, txtNamXem, true)) {
            int selectedRow = bangPhim.getSelectedRow();
            String idPhim = bangPhim.getValueAt(selectedRow, 0).toString();
            String thoiLuong = txtThoiLuongXem.getText();
            String theLoai = (String) jcbTheLoaiXem.getSelectedItem();
            String tenPhim = txtTenPhimXem.getText();
            String tacGia = txtTacGiaXem.getText();
            String nam = txtNamXem.getText();
            String quocGia = txtQuocGiaXem.getText();
            String ngonNgu = txtNgonNguXem.getText();
            String moTa = txtMoTaXem.getText();
            String trangThai = (String) trangThaiXem.getSelectedItem();
            if (trangThai.equals("Ngưng phát hành") && movieBUS.findByExistsShowtime(idPhim)) {
                JOptionPane.showMessageDialog(null, "Phim đang có suất chiếu trong tương lai", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Movie movieOld = movieBUS.getMovieById(idPhim);
            if(!movieOld.getName().equals(tenPhim) && movieBUS.existsByName(tenPhim)){
                JOptionPane.showMessageDialog(null, "Tên Phim đã tồn tại", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if(movieOld.getRunningTime() != Integer.parseInt(thoiLuong) && movieBUS.findByExistsShowtime(idPhim)){
                JOptionPane.showMessageDialog(null, "Không thể sửa thời lượng vì phim đang có suất chiếu trong tương lai", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Movie movieNew;
            if (selectedFileSua == null) { // Không update ảnh
                movieNew = new Movie(idPhim, tenPhim, Integer.parseInt(thoiLuong),
                        quocGia, ngonNgu, moTa, Integer.parseInt(nam), tacGia, movieOld.getPoster(),
                        trangThai.equals("Đang phát hành") ? MovieStatus.PLAYING : MovieStatus.STOPPED, movieTypeBUS.getMovieTypeByName(theLoai));
            } else {
                String image = fileManager.updateFile(selectedFileSua, movieOld.getPoster());
                if (image == null) {
                    JOptionPane.showMessageDialog(null, "Không phải file ảnh", "Lỗi", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                movieNew = new Movie(idPhim, tenPhim, Integer.parseInt(thoiLuong),
                        quocGia, ngonNgu, moTa, Integer.parseInt(nam), tacGia, image,
                        trangThai.equals("Đang phát hành") ? MovieStatus.PLAYING : MovieStatus.STOPPED, movieTypeBUS.getMovieTypeByName(theLoai));
            }
            if (movieBUS.updateMovie(movieNew)) {
                JOptionPane.showMessageDialog(null, "Cập nhật thành công");
                jcbTT.setSelectedIndex(0);
                loadData("", MovieStatus.PLAYING);
                modalSuaPhim.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Cập nhật thất bại", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSuaphimActionPerformed

