    private void loadData(String keyword, MovieStatus movieStatus) {
        modelTable.setRowCount(0);
        java.util.List<Movie> movies = movieBUS.getMovieByKeywordAndMovieStatus(keyword, movieStatus);
        for (Movie movie : movies) {
            MovieType movieType = movieTypeBUS.getMovieTypeById(movie.getMovieType().getMovieTypeId());
            modelTable.addRow(new Object[]{movie.getMovieId(), movie.getName(), movie.getAuthor(), movieType.getName(),
                movie.getCountry(), movie.getRunningTime() + " phút", movie.getMovieStatus() == MovieStatus.PLAYING ? "Đang phát hành"
                : "Ngưng phát hành"});
        }
        lblKetqua.setText(movies.size() + "");
    }


