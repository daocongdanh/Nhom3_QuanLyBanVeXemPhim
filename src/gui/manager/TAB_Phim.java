        txtNamXem.setText(movie.getReleaseYear() + "");
        txtQuocGiaXem.setText(movie.getCountry());
        txtNgonNguXem.setText(movie.getLanguage());
        txtMoTaXem.setText(movie.getDescription());
        if (movie.getMovieStatus().equals(MovieStatus.PLAYING)) {
            trangThaiXem.setSelectedIndex(0);
        } else {
            trangThaiXem.setSelectedIndex(1);
        }

        String imagePath = "/img/" + movie.getPoster();
